   <h2 align="center"><b>Danish and Norwegian students in South Africa</b></h2>
   <img src="thumbnails/pic Open Air.JPG" class="displays">
<p align="center" style="font-size:17px">
We have built up a network of schools that are able to host Danish and Norwegian teacher students doing their practice abroad.<br>
The schools in Durban are Penzance Primary, Glenwood Preparatory, Glenwood Junior, Berea Primary and Open Air School.<br>
The schools in Kwa Swayimane are Ngabayena Primary and Mbava Primary.<br>
The Danish students have participated in the exchange program since 2005 and the Norwegian students since 2014.<br>
The students come from University College VIA, University College Lillebaelt and University College Bergen.<br><br>

